import {field, document} from '../mongoose/decorators';
import {jsonignore} from '../core/decorators';
import {Types} from 'mongoose';

export class AuditableEntity {
    @field({ primary: true, autogenerated: true })
    _id: Types.ObjectId;

    @field()
    name: String;

    @field({ itemType: String })
    courses: Array<String>;

    @field()
    email: String;

    @field()
    accessToken: String;

    @field()
    refreshToken: String;

    @field()
    password: String;
}

export default AuditableEntity;